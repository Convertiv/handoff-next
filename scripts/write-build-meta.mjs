#!/usr/bin/env node
/**
 * Pre-build script: bakes git commit hash + build date into generated
 * TypeScript constants so the CLI and registry app can show a precise version.
 *
 * Outputs:
 *   src/generated/build-meta.ts       — imported by cli.ts (compiled to dist/)
 *   src/app/lib/generated/build-meta.ts — imported by the Next.js app
 *
 * Version format:  2.0.0-alpha.YYYYMMDD.{git-short-hash}
 * Falls back to a timestamp-only suffix when git is unavailable.
 *
 * Both output files are .gitignored — they are regenerated on every build.
 */

import { execSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');

// ── derive build identifiers ────────────────────────────────────────────────

const BASE_VERSION = '2.0.0-alpha';

function gitShortHash() {
  try {
    return execSync('git rev-parse --short=8 HEAD', { cwd: root, stdio: ['ignore', 'pipe', 'ignore'] })
      .toString()
      .trim();
  } catch {
    return null;
  }
}

const now = new Date();
const datePart = [
  now.getFullYear(),
  String(now.getMonth() + 1).padStart(2, '0'),
  String(now.getDate()).padStart(2, '0'),
].join('');

const hash = gitShortHash() ?? now.getTime().toString(16).slice(-8);
const BUILD_VERSION = `${BASE_VERSION}.${datePart}.${hash}`;
const BUILD_DATE = now.toISOString();

// ── file template ────────────────────────────────────────────────────────────

const content = `// AUTO-GENERATED by scripts/write-build-meta.mjs — do not edit.
// Regenerated on every \`npm run build\`.

/** Semver + alpha suffix stamped at build time. e.g. "2.0.0-alpha.20260609.da2f104f" */
export const BUILD_VERSION = ${JSON.stringify(BUILD_VERSION)};

/** ISO 8601 timestamp of this build. */
export const BUILD_DATE = ${JSON.stringify(BUILD_DATE)};
`;

// ── write outputs ────────────────────────────────────────────────────────────

const targets = [
  path.join(root, 'src', 'generated', 'build-meta.ts'),
  path.join(root, 'src', 'app', 'lib', 'generated', 'build-meta.ts'),
];

for (const target of targets) {
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.writeFileSync(target, content, 'utf8');
}

console.log(`write-build-meta: ${BUILD_VERSION}`);
